package com.beesinergi.model;



public class BookPublisher {

    private Integer pkBookPublisher;
    private String publisherCode;
    private String publisherName;
    private String publisherAddress;
    private String publisherPhoneNo;
    
	public Integer getPkBookPublisher() {
		return pkBookPublisher;
	}
	public void setPkBookPublisher(Integer pkBookPublisher) {
		this.pkBookPublisher = pkBookPublisher;
	}
	public String getPublisherCode() {
		return publisherCode;
	}
	public void setPublisherCode(String publisherCode) {
		this.publisherCode = publisherCode;
	}
	public String getPublisherName() {
		return publisherName;
	}
	public void setPublisherName(String publisherName) {
		this.publisherName = publisherName;
	}
	public String getPublisherAddress() {
		return publisherAddress;
	}
	public void setPublisherAddress(String publisherAddress) {
		this.publisherAddress = publisherAddress;
	}
	public String getPublisherPhoneNo() {
		return publisherPhoneNo;
	}
	public void setPublisherPhoneNo(String publisherPhoneNo) {
		this.publisherPhoneNo = publisherPhoneNo;
	}
    
    
    
}